// •	Чему равна сумма [ ] + 1 + 2?
// let b = [] + 1 + 2;
// console.log(b);
// Выведет в консоль 12, т.к. данное арифметическое выражение будет считаться как string, тем самым просто склеивает значения (1 + 2 = 12) как строки, из за того что данный массив содержащийся в выражении не считается числовым значением.

// •	Что выведет этот код: alert( "1"[0] )?
// alert( "1"[0] );
// Выведет модальное окно со строковым значением "1", т.к. в указанном после него массиве под нулевым элементом ничего не указанно (соответственно массив пустой).

// •	Чему равно 2 && 1 && null && 0 && undefined ?
// alert(2 && 1 && null && 0 && undefined);
// Выведет в модальное окно null, т.к. оператор && проверяет операнды с лева направо и останавливается на false(ложь), в случает если все операнды true(правда), тогда выводит последнее значение.

// •	Есть ли разница между выражениями? !!( a && b ) и (a && b)?
// Разници нет, т.к. !! знак конвертирует данное значение, то есть !!(a && b) = (a && b)

// •	Что выведет этот код: alert( null || 2 && 3 || 4 ); ?
// alert( null || 2 && 3 || 4 );
// Выведет модальное окно со значением "3", т.к. приоритет && больше чем у ||, соответственно оно выполниться раньше и получит вид null || 3 || 4, далее выведет "3" - т.к. это первое значение в логическом контексте выдаст true(правда), т.к. || останавливается на первом true.


